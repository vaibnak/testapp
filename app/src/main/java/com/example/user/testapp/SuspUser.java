package com.example.user.testapp;

public class SuspUser {
    String mob;
    String durl;

    public SuspUser() {
    }

    public SuspUser(String mob, String durl) {
        this.mob = mob;
        this.durl = durl;
    }

    public String getMob() {
        return mob;
    }

    public String getDurl() {
        return durl;
    }
}
